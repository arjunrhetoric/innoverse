// Custom function to add CSS variables for Tailwind colors
function addVariablesForColors({ addBase, theme }) {
  const allColors = flattenColorPalette(theme("colors")); // Retrieve all colors
  const newVars = Object.fromEntries(
    Object.entries(allColors).map(([key, val]) => [`--${key}`, val])
  );

  addBase({
    ":root": newVars, // Add all variables to :root
  });
}
